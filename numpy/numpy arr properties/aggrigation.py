"""arr functions
np.sum() - sum of all elements in the array
np.prod() - product of all elements in the array
np.mean() - mean of all elements in the array
np.median() - median of all elements in the array
np.std() - standard deviation of all elements in the array
np.var() - variance of all elements in the array
np.min() - minimum value in the array
np.max() - maximum value in the array
np.argmin() - index of the minimum value in the array
np.argmax() - index of the maximum value in the array

"""
import numpy as np
arr=np.array([1,2,3,4,5])
print(np.sum(arr))
print(np.prod(arr))
print(np.mean(arr))
print(np.median(arr))
print(np.std(arr))
print(np.var(arr))
print(np.min(arr))
print(np.max(arr))
print(np.argmin(arr))
print(np.argmax(arr))